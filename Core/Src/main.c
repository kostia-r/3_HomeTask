void IndicateADC(uint32_t green_duty_cycle, uint32_t orange_duty_cycle, uint32_t blue_duty_cycle)
{
	TIM4->CCR1 = green_duty_cycle; //external temp
	TIM4->CCR2 = orange_duty_cycle; //internal temp
	TIM4->CCR4 = blue_duty_cycle; //potentiometer voltage
	emergency_tot = channel_1_state + channel_2_state + channel_3_state; //total number of active emergencies
	if (emergency_tot == 0 && HAL_GPIO_ReadPin(GPIOD, LED_RED_Pin) == GPIO_PIN_SET)
		HAL_GPIO_WritePin(GPIOD, LED_RED_Pin, GPIO_PIN_RESET);
	switch (emergency_tot)
	{
	case 1: //RED emergency blinking frequency 1 Hz (duty cycle 500ms/1000ms =50%)
		if (emergency_timer >= 500)
		{
			HAL_GPIO_TogglePin(GPIOD, LED_RED_Pin);
			emergency_timer = 0;
		}

		break;
	case 2: //RED emergency blinking frequency 2.5 Hz (duty cycle 200ms/400ms =50%)
		if (emergency_timer >= 200)
		{
			HAL_GPIO_TogglePin(GPIOD, LED_RED_Pin);
			emergency_timer = 0;
		}

		break;
	case 3: //RED emergency blinking frequency 5 Hz (duty cycle 100ms/200ms =50%)
		if (emergency_timer >= 100)
		{
			HAL_GPIO_TogglePin(GPIOD, LED_RED_Pin);
			emergency_timer = 0;
		}
		break;
	}
}

uint32_t GetPotentiometerVoltage(void)
{
	volatile HAL_StatusTypeDef adcPoolResult;
	uint32_t valuePot = 0;
	HAL_ADC_Start(&hadc3);
	adcPoolResult = HAL_ADC_PollForConversion(&hadc3, 1);
	if (adcPoolResult == HAL_OK)
	{
		valuePot = (HAL_ADC_GetValue(&hadc3) * 100) / 4095;
		if ((valuePot) > CRITICAL_POT_VALUE)
		{
			channel_1_state = EMERGENCY_SIT;
		}
		else if ((valuePot + HYSTERESIS_POT) < CRITICAL_POT_VALUE)
		{
			channel_1_state = NORMAL;
		}
	}
	HAL_ADC_Stop(&hadc3);
	return valuePot;
}

uint32_t GetInternalTemperature(void)
{
	volatile HAL_StatusTypeDef adcPoolResult;
	uint32_t internalTemp = 0, valueInTemp = 0;
	double voltageInTemp;
	HAL_ADC_Start(&hadc1);
	adcPoolResult = HAL_ADC_PollForConversion(&hadc1, 100);
	if (adcPoolResult == HAL_OK)
	{
		valueInTemp = HAL_ADC_GetValue(&hadc1); // read value from internal temperature sensor
		voltageInTemp = (double) valueInTemp * (3.3 / 4096); //convert valueInTemp to voltage
		internalTemp = (voltageInTemp - 0.76) / 0.0025 + 25; //convert voltage to temperature in 째C
		if (internalTemp > CRITICAL_INT_TEMP)
		{
			channel_2_state = EMERGENCY_SIT;
		}
		else if ((internalTemp + HYSTERESIS_INT_TEMP) < CRITICAL_INT_TEMP)
		{
			channel_2_state = NORMAL;
		}
	}
	HAL_ADC_Stop(&hadc1);
	return internalTemp;
}

uint32_t GetExternalTemperature(void)
{
	volatile HAL_StatusTypeDef adcPoolResult;
	uint32_t externalTemp = 0, valueExTemp = 0;
	HAL_ADC_Start(&hadc2);
	adcPoolResult = HAL_ADC_PollForConversion(&hadc2, 100);
	if (adcPoolResult == HAL_OK)
	{
		valueExTemp = HAL_ADC_GetValue(&hadc2); // read value from external temperature sensor
		externalTemp = ((4095 - valueExTemp) * 124) / 4095 - 24; //convert value to temperature in the range from -24 to 100째C.
		if (externalTemp > CRITICAL_EXT_TEMP)
		{
			channel_3_state = EMERGENCY_SIT;
		}
		else if ((externalTemp + HYSTERESIS_EXT_TEMP) < CRITICAL_EXT_TEMP)
		{
			channel_3_state = NORMAL;
		}
	}
	HAL_ADC_Stop(&hadc2);
	return externalTemp;
}
